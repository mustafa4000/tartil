    <b style="color: #2F2F2F;">About Abdul Muhsin Al Qasim</b>
    <p style="color: #2F2F2F;">Abdul Muhsin al Qasim was born in the city of Makkah, in the year 1388 Hijrah, Sheikh Abdul Muhsin al Qasim was raised under the auspices of Islamic science because of the involvement of his father and grandfather in a scientific group (Fatwa Syekh Al-Islam</p>
    <p style="color: #2F2F2F;">Born in the city of Makkah, in the year 1388 Hijrah, Sheikh Abdul Muhsin al Qasim was raised under the auspices of Islamic science due to the involvement of his father and grandfather in scientific groups (Fatwa Syekh Al-Islam Ibn Taymiyyah and Fatwa of the Imams of Najd).</p>
    <p style="color: #2F2F2F;">The Shaykh memorized the Koran and became a student of science at a very young age. He studied under prominent Syuyookhs, which included Shaykh Abdullah bin Humaid Rahimahullah, Abdul-Aziz bin Abd-Allah bin Baz Rahimahullah, Shaykh Salih bin Ali An-Nasir Rahimahullah and Sheikh Al-Muhaddith Abdullah As-Sa'd.</p>
    <p style="color: #2F2F2F;">In 1410 Hijrah, he graduated from the Sharia College in Riyadh and obtained his master's degree in 1413.</p>
    <p style="color: #2F2F2F;">He started his career as a judge in Riyadh and at Tabook and after that, was appointed to the prestigious position of Imam of the Masjid An-Nabawi.</p>
    <p style="color: #2F2F2F;">In addition, the Shaykh has written several books; Some of them include Khutub Al-Munbariyya (khutbah), Facilitating Achievements in the Explanation of the Three Usools, Hanafi Fiqh Books, Annotations Formulated by Ulama's Work in Hanafi Fiqh and others.</p>
    <p style="color: #2F2F2F;">The sheikh was an honorable reciter, wise judge, eloquent khateeb, and a man known for his humble character and diligent study.</p>
